package org.quickbundle.modules.mobile.phonegapdemo;

public class ReceiptInfo {

	private String receiptNum;
	private String receiptTitle;
	private String receiptDetail;
	private String receiptApplicant;
	private String receiptDate;
	/**
	 * @return the receiptNum
	 */
	public String getReceiptNum() {
		return receiptNum;
	}
	/**
	 * @param receiptNum the receiptNum to set
	 */
	public void setReceiptNum(String receiptNum) {
		this.receiptNum = receiptNum;
	}
	/**
	 * @return the receiptTitle
	 */
	public String getReceiptTitle() {
		return receiptTitle;
	}
	/**
	 * @param receiptTitle the receiptTitle to set
	 */
	public void setReceiptTitle(String receiptTitle) {
		this.receiptTitle = receiptTitle;
	}
	/**
	 * @return the receiptDetail
	 */
	public String getReceiptDetail() {
		return receiptDetail;
	}
	/**
	 * @param receiptDetail the receiptDetail to set
	 */
	public void setReceiptDetail(String receiptDetail) {
		this.receiptDetail = receiptDetail;
	}
	/**
	 * @return the receiptApplicant
	 */
	public String getReceiptApplicant() {
		return receiptApplicant;
	}
	/**
	 * @param receiptApplicant the receiptApplicant to set
	 */
	public void setReceiptApplicant(String receiptApplicant) {
		this.receiptApplicant = receiptApplicant;
	}
	/**
	 * @return the receiptDate
	 */
	public String getReceiptDate() {
		return receiptDate;
	}
	/**
	 * @param receiptDate the receiptDate to set
	 */
	public void setReceiptDate(String receiptDate) {
		this.receiptDate = receiptDate;
	}
}
